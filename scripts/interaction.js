async function interact() {
    const web3 = await new Web3("http://data-seed-prebsc-2-s2.binance.org:8545");
    const floppyContract = await new web3.eth.Contract(floppyAbi, floppyAddress);

    //Call
    const myBalance = await  floppyContract.methods.balanceOf(myAddress).call();
    console.log('[myBalance]', myBalance);

    //Send

    await web3.eth.accounts.wallet.add(myPrivateKey);
    const receiverBalance = await floppyContract.methods.balanceOf(receiverAddress).call();

    const rs = await floppyContract.methods.transfer(receiverAddress, 10000).send({
        from: myAddress,
        gas: 3000000
    });

    const receiverBalanceAfter = await floppyContract.methods.balanceOf(receiverAddress).call();
    console.log('[RS]',rs);
    console.log('[receiverBalance]',receiverBalance);
    console.log('[receiverBalanceAfter]',receiverBalanceAfter);
}

